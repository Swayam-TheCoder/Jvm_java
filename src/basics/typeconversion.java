package basics;

public class typeconversion {
    public static void main(String[] args) {
        float flo = 21;
        System.out.println(flo); // Automatic Type conversion (Implicit Conversionch) or Widening or coercion

        int in = (int) 2.772329;
        System.out.println(in); // Explicit Conversion or Narrowing or casting
    }
}
